// main function that is the first point of entry into the program
fn main() {
    // 'println!' is a macro that prints the string to the console
    // '!' is used to to indicate that this is a macro
    println!("hello, world!");
}

// compile the program with:
// `rustc main.rs`
//
// run the program with:
// `./main` or `./main.exe`
// 
// Windows will also output a debug information in the `.pdb` file